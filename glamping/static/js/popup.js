document.addEventListener("DOMContentLoaded", function () {
  // Ждём, пока весь HTML-документ будет полностью загружен и разобран браузером.
  // Это нужно, чтобы элементы страницы уже существовали, когда мы будем к ним обращаться.

  var container = document.querySelector(".popup-container");
  // Находим элемент контейнера попапа по классу .popup-container и сохраняем его в переменную container.
  // Этот элемент будет использоваться для показа/скрытия всплывающего окна.

  var popupButtons = document.querySelectorAll(".open-popup");
  // Находим все элементы (кнопки/ссылки) с классом .open-popup и сохраняем их в NodeList.
  // NodeList — это массивоподобный объект, поэтому можно перебирать элементы.

  for (let i = 0; i < popupButtons.length; i++) {
    // Запускаем цикл по всем найденным кнопкам pop-up.

    popupButtons[i].addEventListener("click", function () {
      // Добавляем обработчик события 'click' для каждой кнопки.
      // Когда пользователь нажимает на кнопку, выполняется функция.

      container.style.display = "flex";
      // Показываем контейнер попапа, меняя его стиль display на 'flex'.
      // 'flex' используется, чтобы контейнер с попапом центрировался (см. CSS).
    });
  }

  container.addEventListener("click", function (event) {
    // Добавляем обработчик клика по самому контейнеру pop-up.
    // Это нужно для того, чтобы закрывать попап при клике на фон, а не на саму форму.

    if (event.target == container) {
      // Проверяем, что клик был именно по контейнеру, а не по его внутренним элементам (например, форме).
      // Если кликнули по пустому фону, то выполняем скрытие попапа.

      container.style.display = "none";
      // Скрываем контейнер, устанавливая display: none.
    }
  });
});
